#include <unistd.h>
#include "libft.h"

void	*ft_memset(void *str, int c, size_t n)
{
	size_t			i;

	i = 0;
	while (i < n)
	{
		((char *)str)[i] = (char)c;
		i++;
	}
	return (str);
}
